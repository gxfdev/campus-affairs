def get_major(class_name):
    """从班级名提取专业代码"""
    for prefix in ["计科", "通信", "网工", "物联", "数据", "电信", "会计", "土木", "建筑", "日语", "英语", "营销", "工商"]:
        if class_name.startswith(prefix):
            return prefix
    return None

def get_grade(class_name):
    """从班级名提取年级"""
    for grade in ["23", "24"]:
        if grade in class_name:
            return grade
    return None

def generate_schedule_for_class_semester(class_name, semester, semester_idx, existing_count):
    """为一个班级的一个学期生成课表记录"""
    major = get_major(class_name)
    if major not in COURSES:
        return []

    courses = COURSES[major].get(semester_idx, [])
    if not courses:
        return []

    records = []
    used_teachers = set()
    used_slots = set()  # (day, period_start) 避免时间冲突

    for course_name, credit, course_type, (week_start, week_end) in courses:
        # 每门课安排1-2个时间段
        num_sessions = 2 if credit >= 4.0 else 1
        for _ in range(num_sessions):
            # 随机选择教师（避免重复）
            available_teachers = [t for t in TEACHERS if t not in used_teachers]
            if not available_teachers:
                available_teachers = TEACHERS[:]
                used_teachers.clear()
            teacher = random.choice(available_teachers)
            used_teachers.add(teacher)

            # 随机选择教室
            location = random.choice(LOCATIONS)

            # 随机选择时间（周一到周五，1-5节或6-10节）
            attempts = 0
            while attempts < 50:
                day = random.randint(1, 5)
                period_start = random.choice([1, 3, 6, 8])
                period_end = period_start + 1
                slot_key = (day, period_start)
                if slot_key not in used_slots:
                    used_slots.add(slot_key)
                    break
                attempts += 1
            else:
                continue  # 跳过如果找不到空闲时间段

            records.append({
                "class_name": class_name,
                "course_name": course_name,
                "teacher_name": teacher,
                "location": location,
                "day_of_week": day,
                "period_start": period_start,
                "period_end": period_end,
                "semester": semester,
                "credit": credit,
                "course_type": course_type,
                "week_start": week_start,
                "week_end": week_end,
            })

    return records

def main():
    random.seed(42)  # 固定随机种子确保可重复

    # 读取现有课表数据
    existing = {}
    with open("schedule_matrix.txt", "r", encoding="utf-8") as f:
        lines = f.readlines()[1:]  # 跳过表头
        for line in lines:
            parts = line.strip().split("\t")
            if len(parts) >= 3:
                class_name, semester, cnt = parts[0], parts[1], int(parts[2])
                existing[(class_name, semester)] = cnt

    all_records = []

    for class_name in ALL_CLASSES:
        grade = get_grade(class_name)
        if not grade:
            continue
        semesters = SEMESTER_MAP[grade]
        major = get_major(class_name)

        for idx, semester in enumerate(semesters, 1):
            # 检查是否已有足够的课表数据（>=10条认为已有）
            existing_count = existing.get((class_name, semester), 0)
            if existing_count >= 10:
                continue

            # 如果有少量数据（<10），先删除再重新生成
            if existing_count > 0 and existing_count < 10:
                # 标记需要删除
                pass

            records = generate_schedule_for_class_semester(class_name, semester, idx, existing_count)
            all_records.extend(records)

    # 生成SQL
    sql_lines = []
    sql_lines.append("-- 课表数据补全脚本 - 参考广东石油化工学院课程体系")
    sql_lines.append("-- 生成时间: 2026-06-19")
    sql_lines.append("USE campus_affairs;")
    sql_lines.append("")

    # 先删除不完整的课表数据（少于10条的）
    sql_lines.append("-- 删除不完整的课表数据（少于10条的班级-学期组合）")
    delete_sql = """DELETE FROM course_schedule WHERE deleted=0 AND (class_name, semester) IN (
    SELECT t.class_name, t.semester FROM (
        SELECT class_name, semester, COUNT(*) as cnt
        FROM course_schedule WHERE deleted=0
        GROUP BY class_name, semester
        HAVING cnt < 10
    ) t
);"""
    sql_lines.append(delete_sql)
    sql_lines.append("")

    # 批量插入新数据
    sql_lines.append("-- 批量插入完整课表数据")
    sql_lines.append("INSERT INTO course_schedule (class_name, course_name, teacher_name, location, day_of_week, period_start, period_end, semester, credit, course_type, week_start, week_end) VALUES")

    value_lines = []
    for r in all_records:
        line = f"('{r['class_name']}', '{r['course_name']}', '{r['teacher_name']}', '{r['location']}', {r['day_of_week']}, {r['period_start']}, {r['period_end']}, '{r['semester']}', {r['credit']}, '{r['course_type']}', {r['week_start']}, {r['week_end']})"
        value_lines.append(line)

    # 每批1000条
    batch_size = 1000
    for i in range(0, len(value_lines), batch_size):
        batch = value_lines[i:i+batch_size]
        sql_lines.append(",".join(batch) + ";")
        if i + batch_size < len(value_lines):
            sql_lines.append("")
            sql_lines.append("INSERT INTO course_schedule (class_name, course_name, teacher_name, location, day_of_week, period_start, period_end, semester, credit, course_type, week_start, week_end) VALUES")

    sql_lines.append("")
    sql_lines.append(f"-- 共生成 {len(all_records)} 条课表记录")

    with open("fix_schedule.sql", "w", encoding="utf-8") as f:
        f.write("\n".join(sql_lines))

    print(f"生成完成: {len(all_records)} 条课表记录")
    print(f"SQL文件: fix_schedule.sql")

    # 统计
    stats = {}
    for r in all_records:
        key = (r['class_name'], r['semester'])
        stats[key] = stats.get(key, 0) + 1

    print(f"\n各班级-学期课表记录数:")
    for key in sorted(stats.keys()):
        print(f"  {key[0]} {key[1]}: {stats[key]}条")

if __name__ == "__main__":
    main()
